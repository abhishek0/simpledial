package com.example.divx;

public class VideoInfo {
	public String icon_url; 
	public String title;
	public String url;
	
	public VideoInfo() {
		super();
	}
	
	public VideoInfo(String icon_url, String title, String url) {
		super();
		this.icon_url = icon_url;
		this.title = title;
		this.url = url;
	}
}
